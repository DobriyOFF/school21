void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	long int c;

	c = nb;
	if (c > 2147483647 || c < -2147483648)
		return ;
	if (c < 0)
	{
		c = c * -1;
		ft_putchar('-');
	}
	if (c < 10)
	{
		ft_putchar(c + '0');
		return ;
	}
	ft_putnbr(c / 10);
	ft_putchar((c % 10) + '0');
	return ;
}
